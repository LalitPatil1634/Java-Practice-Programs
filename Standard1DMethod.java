package Array;

public class Standard1DMethod {
    public static void main(String[] args) {
        int[] arr = new int[3];
        arr[0] = 3;
        arr[1] = 4;
        arr[2] = 5;

        System.out.println("One dimensional array elements are: ");
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
    }
}
